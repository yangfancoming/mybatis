
package org.apache.ibatis.submitted.manyanno;

public class AnnoPostTag {

  private int id;
  private String name;

  public AnnoPostTag(int id, String name) {
    this.id = id;
    this.name = name;
  }

  public int getId() {
    return id;
  }

  public String getName() {
    return name;
  }
}
